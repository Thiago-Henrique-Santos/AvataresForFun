    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Criando objeto para guardar os dados da criação de um avatar
        Avatar personagem = new Avatar();
        //Guardadno os dados do avatar
        personagem.setNome(txt_criarNome.getText());
        String idadeValue = spin_criarIdade.getValue().toString(); //Para pegar o valor de um spinner foi necessário pegar em forma de string, primeiro
        personagem.setIdade(Integer.parseInt(idadeValue));
        personagem.setAltura(Float.parseFloat(txt_criarAltura.getText()));
        personagem.setSexo(comboBox_criarSexo.getSelectedItem().toString());
        personagem.setPersonalidade(comboBox_criarPersonalidade.getSelectedItem().toString());
        personagem.setEspecialidade(comboBox_criarEspecialidade.getSelectedItem().toString());
        
        //Fazendo a transação, para adicionar no banco de dados
        entityManager1.getTransaction().begin();
        entityManager1.persist(personagem);
        entityManager1.getTransaction().commit();
        
        //Alertando que o avatr for criado, limpando caixas de texto e atualizando a tabela de personagens, da guia analisar
        JOptionPane.showMessageDialog(this, "Avatar criado com sucesso!", "Avatar Criado", JOptionPane.INFORMATION_MESSAGE);
        criar_setCamposPadrao();
        atualizarTabela();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //Fazendo busca do avatar escolhido, por nome, pelo usuário.
        //Buscando onde (where) o nome do objeto a, declarado, no comando, for igual ao parâmetro nome
        Query consulta = entityManager1.createQuery("select a from Avatar a where a.nome = :nome");
        //Setando o valor para o parâmetro nome
        consulta.setParameter("nome", txt_analiseNome.getText());
        //Guardando a lista de avatares que a variável consulta, do tipo Query, pegar
        List<Avatar> listaResultados = consulta.getResultList();
        //Deixando, na tabela, apenas o avatar escolhido, por nome, pelo usuário
        getAvatarEspecificoNaTabela(listaResultados);
    }//GEN-LAST:event_jButton3ActionPerformed

