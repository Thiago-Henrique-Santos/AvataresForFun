    public void atualizarTabela(){
        //Pegando o modelo da tabela (quantidade de linhas e colunas, e outras propriedades)
        DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
        //Definindo número de linhas para a tabela. No caso retirando todas, como estamos setando 0
        modelo.setNumRows(0);
        
        //Criando uma lista, do tipo avatar, para pegar a lista de todos os avatares no banco de dados, utilizando o query
        List<Avatar> lista_avatar = entityManager1.createQuery("select a from Avatar a").getResultList();
        
        //Andando em toda a lista de avatares
        for(Avatar avatar : lista_avatar){
            //Adicionando linha na tabela. Como parâmetro do vetor de objetos, um atributo do objeto da tabela (no caso Avatar).
            //Os parâmetros são passados na ordem em que aparecerão na tabela.
            //Irá adicionar todos os avatares, pois o loop está rodando em toda a lista de avatares. O comando abaixo é para adicionar um linha
            //(no caso, adicona uma linha em cada vez que passa no loop).
            modelo.addRow(new Object[]{
                    avatar.getIdAvatar(),
                    avatar.getNome(),
                    avatar.getIdade(),
                    avatar.getAltura(),
                    avatar.getSexo(),
                    avatar.getPersonalidade(),
                    avatar.getEspecialidade()
            });
        }
        
        //Atualizando a variável global dadosDaTabela
        dadosDaTabela.clear();
        dadosDaTabela.addAll(lista_avatar);
    }
    
