    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //Pegando a linha selecionada na tabela
        int linhaSelecionada = jTable2.getSelectedRow();
        //Verificando se há linha selecionada
        //Um vetor (ou lista) começa no índice 0, então, se não houver linha selecionada,
        //o getSelectedRow fica com -1
        if(linhaSelecionada >= 0){
            //Pegando a resposta de um JOptionPane de confirmação
            int resp = JOptionPane.showConfirmDialog(this, "Deseja, realmente, alterar este avatar?", "Confirmação", JOptionPane.YES_NO_OPTION);
            //Verificando se a opção selecionada foi sim
            if(resp == JOptionPane.YES_OPTION){
                //Pegando o avatar selecionado
                Avatar avatarSelecionado = dadosDaTabela.get(linhaSelecionada);
                //Guardando os novos valores dos atributos do avatar
                avatarSelecionado.setNome(txt_analiseNome.getText());
                avatarSelecionado.setIdade((Integer) spin_analiseIdade.getValue()); //Guardando o valor pego no spinner de idade, usando um casting para converter para inteiro
                avatarSelecionado.setAltura(Float.parseFloat(txt_analiseAltura.getText()));
                avatarSelecionado.setSexo(comboBox_analiseSexo.getSelectedItem().toString());
                avatarSelecionado.setPersonalidade(comboBox_analisePersonalidade.getSelectedItem().toString());
                avatarSelecionado.setEspecialidade(comboBox_analiseEspecialidade.getSelectedItem().toString());
                //Atualziando o avatar no banco de dados
                entityManager1.getTransaction().begin();
                entityManager1.merge(avatarSelecionado);
                entityManager1.getTransaction().commit();
                //Atualizando a tabela e voltando os campos ao padrão
                atualizarTabela();
                analise_setCamposPadrao();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

